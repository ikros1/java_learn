package java_se.generics;


public class GenericsClassExample<T> {

    private T t;

    public void add(T t) {
        this.t = t;
    }

    public T get() {
        return t;
    }

    public static void main(String[] args) {
        GenericsClassExample<Integer> integerBox = new GenericsClassExample<Integer>();
        GenericsClassExample<String> stringBox = new GenericsClassExample<String>();

        integerBox.add(new Integer(10));
        stringBox.add(new String("菜鸟教程"));

        System.out.printf("整型值为 :%d\n\n", integerBox.get());
        System.out.printf("字符串为 :%s\n", stringBox.get());
    }
}